    private void jBtnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnStartActionPerformed
        // TODO add your handling code here:

        state =true;
        
        // Dá inicio à contagem
        Thread t = new Thread() {
            @Override
            public void run() {
                for (;;) {
                    if (state == true) {
                        try {
                            sleep(1);

                            if (millisenconds > 1000) {
                                millisenconds = 0;
                                seconds++;
                            }
                            if (seconds > 60) {
                                millisenconds = 0;
                                seconds = 0;
                                minutes++;
                            }
                            if (minutes > 60) {
                                millisenconds = 0;
                                seconds = 0;
                                minutes = 0;
                                hour++;
                            }
                            millisenconds++;

                            jLblmili.setText(":" + verificaNumero(millisenconds));
                            jLblsegundo.setText(":" + verificaNumero(seconds));
                            jLblminuto2.setText(":" + verificaNumero(minutes));
                            jLblhora1.setText("" + verificaNumero(hour));

                        } catch (Exception e) {
                        }

                    } else {
                        state = false;
                        break;
                    }
                }

            }
        };
        t.start();

    }//GEN-LAST:event_jBtnStartActionPerformed

