    public String verificaNumero(int intEntrada) {
        String numeroSaida;
        if (0 >= intEntrada || intEntrada <= 9) {
            numeroSaida = "0" + Integer.toString(intEntrada);
            return numeroSaida;
        } else {
            return numeroSaida = Integer.toString(intEntrada);
        }
    }
    
    
    
